/**
 * A simple [Fragment] subclass.
 * Use the [ProfileFragment.newInstance] factory method to
 * create an instance of this fragment.
 */
class ProfileFragment : Fragment() {
    // TODO: Rename and change types of parameters
    private lateinit var name: TextView
    private lateinit var highScore: TextView


    @SuppressLint("SetTextI18n")
    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        val view = inflater.inflate(R.layout.fragment_profile, container, false)

        name = view.findViewById(R.id.player_name)
        highScore = view.findViewById(R.id.high_score)

        name.text = "player name: ${(activity as MainActivity).viewModel.selectedName}"
        highScore.text = "high score: ${(activity as MainActivity).viewModel.highScore}"

        return view
    }

}